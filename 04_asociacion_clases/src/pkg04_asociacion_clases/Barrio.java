package pkg04_asociacion_clases;
public class Barrio {
    public String nombre;
    public String pais;
    public String municipio;
    public String ciudad;
    //constructor
    public Barrio(String nombre, String pais, String municipio, String ciudad) {
        this.nombre = nombre;
        this.pais = pais;
        this.municipio = municipio;
        this.ciudad = ciudad;
    }
}
