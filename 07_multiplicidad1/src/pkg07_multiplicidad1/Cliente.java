package pkg07_multiplicidad1;
public class Cliente {
    //atributos
    public String nombre;
    public String rtn;
    public int telefono;
    public String correo;
    //constructor
    public Cliente(String nombre, String rtn, int telefono, String correo) {
        this.nombre = nombre;
        this.rtn = rtn;
        this.telefono = telefono;
        this.correo = correo;
    }
}
